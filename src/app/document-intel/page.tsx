function detectExtractionType(fileName: string): ExtractedData {
  const lower = fileName.toLowerCase();
  if (lower.includes('po') || lower.includes('purchase') || lower.includes('order')) return MOCK_EXTRACTIONS.po;
  if (lower.includes('exam') || lower.includes('fssai') || lower.includes('notification') || lower.includes('vacancy')) return MOCK_EXTRACTIONS.exam;
  return MOCK_EXTRACTIONS.default;
}

// Drag-and-drop zone component
function DropZone({ onFile }: { onFile: (f: File) => void }) {
  const [isDragging, setIsDragging] = useState(false);

  const handleDrop = useCallback((e: React.DragEvent) => {
    e.preventDefault();
    setIsDragging(false);
    const file = e.dataTransfer.files?.[0];
    if (file) onFile(file);
  }, [onFile]);

  return (
    <div
      onDrop={handleDrop}
      onDragOver={e => { e.preventDefault(); setIsDragging(true); }}
      onDragLeave={() => setIsDragging(false)}
      className={`flex flex-col items-center justify-center border-2 border-dashed rounded-xl p-10 transition-all cursor-default ${
        isDragging
          ? 'border-blue-500 bg-blue-500/5'
          : 'border-zinc-800 bg-zinc-950/20 hover:border-zinc-700'
      }`}
    >
      <div className={`w-12 h-12 rounded-xl flex items-center justify-center mb-3 transition-colors ${isDragging ? 'bg-blue-500/20' : 'bg-zinc-800'}`}>
        <FolderOpen className={`w-6 h-6 ${isDragging ? 'text-blue-400' : 'text-zinc-500'}`} />
      </div>
      <p className="text-sm font-semibold text-zinc-300 mb-1">
        {isDragging ? 'Drop to analyze' : 'Drop file here'}
      </p>
      <p className="text-xs text-zinc-500 mb-5 text-center">PDF, DOCX, XLSX, CSV supported</p>
      <label className="cursor-pointer bg-blue-600 hover:bg-blue-500 text-white font-semibold text-xs px-5 py-2.5 rounded-lg transition-colors flex items-center gap-2">
        <Upload className="w-3.5 h-3.5" />
        Browse File
        <input
          type="file"
          onChange={e => { const f = e.target.files?.[0]; if (f) onFile(f); }}
          className="hidden"
          accept=".pdf,.docx,.xlsx,.csv,.txt"
        />
      </label>
    </div>
  );
}

