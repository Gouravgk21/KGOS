// --- Export helpers ---
function exportAsText(fileName: string, data: ExtractedData) {
  const lines = [
    `KGOS Document Intelligence Export`,
    `Source File: ${fileName}`,
    `Exported: ${new Date().toLocaleString('en-IN')}`,
    `${'─'.repeat(60)}`,
    ``,
    `EXECUTIVE SUMMARY`,
    data.summary,
    ``,
    `TASKS`,
    ...data.tasks.map((t, i) => `  ${i + 1}. ${t}`),
    ``,
    `DEADLINES`,
    ...data.deadlines.map((d, i) => `  ${i + 1}. ${d}`),
    ``,
    `OPPORTUNITIES`,
    ...data.opportunities.map((o, i) => `  ${i + 1}. ${o}`),
    ``,
    `RISKS`,
    ...data.risks.map((r, i) => `  ${i + 1}. ${r}`),
    ``,
    `CONTACTS`,
    ...data.contacts.map((c, i) => `  ${i + 1}. ${c}`),
  ];
  const blob = new Blob([lines.join('\n')], { type: 'text/plain' });
  triggerDownload(blob, `${sanitizeFileName(fileName)}_kgos_export.txt`);
}

